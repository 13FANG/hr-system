package com.shah.hrsystem.util

/**
 * Общий класс для представления данных вместе с их состоянием загрузки.
 * Используется для передачи данных из репозиториев/use-cases во ViewModel и далее в UI.
 * @param T Тип данных.
 */
sealed class Resource<out T> {
    /**
     * Состояние успеха, содержит загруженные данные.
     * @param data Загруженные данные типа T.
     */
    data class Success<out T>(val data: T) : Resource<T>()

    /**
     * Состояние ошибки, содержит сообщение об ошибке и опционально данные (если есть старые).
     * @param message Сообщение об ошибке.
     * @param data Опциональные данные (например, кэшированные), которые были доступны до ошибки.
     */
    data class Error<out T>(val message: String, val data: T? = null) : Resource<T>()

    /**
     * Состояние загрузки, опционально может содержать предыдущие данные.
     * @param data Опциональные данные (например, кэшированные), которые отображаются во время загрузки новых.
     */
    data class Loading<out T>(val data: T? = null) : Resource<T>()

    /**
     * Начальное или неопределенное состояние.
     */
    // object Idle : Resource<Nothing>() // Можно добавить, если нужно начальное состояние
}